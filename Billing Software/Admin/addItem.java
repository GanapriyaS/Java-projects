    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        FileInputStream fis=null;
            
        if(f== null) //if not then null point exception
        {
            JOptionPane.showMessageDialog(rootPane,"Select image","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                fis= new FileInputStream(f);
            }
            catch(FileNotFoundException e)
            {
                System.out.println(e);
            }
        String item_id=jTextField1.getText();
        String item_name=jTextField2.getText();
        String item_price=jTextField3.getText();
        String category=(String)jComboBox1.getSelectedItem();
        String desc=jTextArea1.getText();
        String quantity=jTextField4.getText();
        if(item_name.equals("") || item_price.equals("") || category.equals("Select") || desc.equals("") || quantity.equals(""))
        {
            JOptionPane.showMessageDialog(rootPane,"Enter details!","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
        try(Connection con=dbConnection.getConnect();
                PreparedStatement ps=con.prepareStatement("insert into item values(?,?,?,?,?,?,?)");
            )
        {   
            
            ps.setString(1, item_id);
            ps.setString(2, item_name);
            ps.setString(3, item_price);
            ps.setString(4, category);
            ps.setString(5, desc);
            ps.setString(6, quantity);
            ps.setBinaryStream(7, fis,f.length());
            int i=ps.executeUpdate();
            if(i>0)
            {
                JOptionPane.showMessageDialog(rootPane,"Item added successfully!!!");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextArea1.setText("");
                jComboBox1.setSelectedIndex(0);
                jLabel8.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/index_1.png"))); 
                generateItemId();
                f=null;
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane,"Item not added!","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
        }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

