void generateItemId()
{
     int count=0;
        try(Connection con=dbConnection.getConnect();
                PreparedStatement ps=con.prepareStatement("select count(*) as count from item");
                ResultSet rs=ps.executeQuery();)
        {
            if(rs.next())
            {   
                count=rs.getInt("count");
                jTextField1.setText(""+(count+1));
                System.out.println(count);
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
}
